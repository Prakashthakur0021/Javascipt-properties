// the concat method allow you to merge two or more array into one new array which
// will be return
console.clear();

const a = [1, 2, 3];
const b = [4, 5, 6];
const d = [7, 8, 9];

const c = a.concat(b, d);

console.log("a => " + a);
console.log("b => " + b);
console.log("c => " + c);

e = a.concat(232, 134, b);

